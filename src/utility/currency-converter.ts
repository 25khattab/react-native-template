/**
 *
 * @param num
 * The number to be formatted
 * @param lang
 * The language to be formatted with check 639-1 codes here https://en.wikipedia.org/wiki/ISO_639-1_codes
 * @param currency
 * make sure the code is listed here https://en.wikipedia.org/wiki/ISO_4217#List_of_ISO_4217_currency_codes
 * @description
 * it takes the number and reformat it to specific language and currency
 * @returns formatted number with currency with 2 fraction digits
 */

export function convertCurrency(
  num: number,
  lang: string,
  currency: CurrencyCodes,
) {
  return new Intl.NumberFormat(lang, {
    style: 'currency',
    currency,
    maximumFractionDigits: 2,
    minimumFractionDigits: 0,
  }).format(num);
}

